<h1>Three Stories Down</h1>
<br/>

<p>
<span class="quote-big">“Guyot strikes just the right balance between hardboiled grit,<br/> intelligent mystery and humor...”</span><br/>
<span class="quote">&nbsp;&nbsp;&nbsp;&mdash;Geoffrey Thorne, Author Knight Rider comic book series</span>
</p>

<p>
<span class="dropcap" style="letter-spacing: 0px;">I</span>n “The Closers,” Paul Guyot weaves a dark satire of America's corporate culture, where salespeople close deals not for stocks or bonds, but assassinations.
</p>

<p>
“Barry of Hollywood” is a throwback to old private eye novels, with golf pro turned gumshoe James Hooker revisiting the city he was exiled from to solve the murder of a friend.
</p>

<p>
“The Law and the Order” spins a nasty tale of Murphy's Law when a guy with no luck, no direction and no brains just tries to get a little bit ahead. 
</p>


